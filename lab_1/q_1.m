clc;
close all;
clear all;
t = 0:1:4;
subplot(3,3,1);
u = (t>=0);
stem(t,u);
title('unit step');
grid on;
subplot(3,3,2);
stem(t,cos(pi*t));
title('cos(pi*n)');
grid on;
subplot(3,3,3);
stem(t,cos(0.1*pi*t));
title('cos(0.1*pi*t)');
grid on;
subplot(3,3,4);
stem(t,cos(0.1*t));
title('cos(0.1*t)');
grid on;
subplot(3,3,5);
f = (t==0);
stem(t,f);
xlim([-1 5]);
grid on;
title('impulse function');
a = [1 2 3 4 5];
subplot(3,3,6);
stem(t,a);
title('x[n]');
grid on;
subplot(3,3,9);
stem(-t,a);
grid on;
title('x[-n]');
subplot(3,3,8);
stem(t-2,a);
grid on;
title('x(n-2)');
subplot(3,3,7);
stem(t*2,a);
title('x[2*n]');
grid on;
